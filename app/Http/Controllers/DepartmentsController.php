    {
        $request->validate([
            'id'=>'required',
            'level'=>'required',
            'email'=>'required',
            'name'=>'required'
        ]);

        $contact = new Contact([
            'id' => $request->get('dept_id'),
            'email' => $request->get('dept_email'),
            'name' => $request->get('dept_name'),
            'level' => $request->get('dept_level'),
            'date' => $request->get('d_date')
            
        ]);
        $contact->save();
        return redirect('/departments')->with('success', 'Department Created successfully!');   
